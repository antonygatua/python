a = 7 
print(type(a))

b = 5.0
print(type(b))

# Python automatically converts 
# c to float as it is a float addition 
c = a + b 
print(c) 
print(type(c))
 
# Python automatically converts 
# d to float as it is a float multiplication
d = a * b
print(d)
print(type(d))

# int to float 
f = 4
x = float(f)
print(type(x))

# float to int 
g = 2.7
y = int(g)
print(y, type(y))

# int to string 
pie = 3.142
print(str(pie))