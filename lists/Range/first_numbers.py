# making Numerical Lists
# using the range() function - makes it easier to generate a series of numbers
for value in range(1, 5): # only numbers 1 through 4
    print(value)

# using range() to make a list of numbers
# convert the results of range() directly into a list using the list() function
numbers = list(range(1,6))
print(numbers)

# Note: range() format - range(start, stop, step) 
# Default step is 1 